def number_to_month(month):
    if month == 1:
        name_month = "enero"
    elif month == 2:
        name_month = "febrero"
    elif month == 3:
        name_month = "marzo"
    elif month == 4:
        name_month = "abril"
    elif month == 5:
        name_month = "mayo"
    elif month == 6:
        name_month = "junio"
    elif month == 7:
        name_month = "julio"
    elif month == 8:
        name_month = "agosto"
    elif month == 9:
        name_month = "septiembre"
    elif month == 10:
        name_month = "octubre"
    elif month == 11:
        name_month = "noviembre"
    elif month == 12:
        name_month = "diciembre"
    else:
        name_month = "error"
    return name_month

